public class ScanFreqProgram {

    private String pid;
    private String name;
    private String ca;
    private String vpid;
    private String venc;
    private String apid;
    private String aenc;

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCa() {
        return ca;
    }

    public void setCa(String ca) {
        this.ca = ca;
    }

    public String getVpid() {
        return vpid;
    }

    public void setVpid(String vpid) {
        this.vpid = vpid;
    }

    public String getVenc() {
        return venc;
    }

    public void setVenc(String venc) {
        this.venc = venc;
    }

    public String getApid() {
        return apid;
    }

    public void setApid(String apid) {
        this.apid = apid;
    }

    public String getAenc() {
        return aenc;
    }

    public void setAenc(String aenc) {
        this.aenc = aenc;
    }
}
